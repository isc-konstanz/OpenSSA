package org.esg.ic.ssa;

public class GenericAdapterComException extends GenericAdapterException {
	private static final long serialVersionUID = 8729306678424993937L;

	public GenericAdapterComException() {
		super();
	}

	public GenericAdapterComException(String s) {
		super(s);
	}

	public GenericAdapterComException(Throwable cause) {
		super(cause);
	}

	public GenericAdapterComException(String s, Throwable cause) {
		super(s, cause);
	}
}
